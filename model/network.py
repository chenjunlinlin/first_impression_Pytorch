class Lstm_modle(nn.Module):
    def __init__(self, input, out_put, *args, **kwargs) -> None:
        super(Lstm_modle, self).__init__(*args, **kwargs)
        self.lstm1 = nn.LSTM(input, 128, batch_first=True)
        self.lstm2 = nn.LSTM(128, out_put, batch_first=True)

    def forward(self, input):
        X1, _ = self.lstm1(input)
        X2, _ = self.lstm2(X1)

        return X2


def warmup(optimizer, Lr, total_epoch, cur_epoch):
    '''After total_epoch iterations, Lr changes from 0 to Lr,
        return the current Lr at cur_epoch
    '''
    part = Lr / total_epoch
    cur_Lr = part * cur_epoch
    if cur_epoch <= total_epoch:
        for para_group in optimizer.param_groups:
            para_group["lr"] = cur_Lr
        print(f'------------learning rate :{cur_Lr:.4f}')
